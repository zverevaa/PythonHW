# –ü–æ –≥—Ä–∞—Ñ–∏–∫—É –æ–ø—Ä–µ–¥–µ–ª–∏—Ç–µ, –ø—Ä–∏ –∫–∞–∫–∏—Ö –∑–Ω–∞—á–µ–Ω–∏—è x –∑–Ω–∞—á–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ.
import numpy as np
import matplotlib.pyplot as plt
x = []
y = []


for i in range(-10, 10):
    x.append(i)
for i in range(len(x)):
    y.append(5 * x[i] * x[i] + 10 * x[i] - 30)

print(f'–ú–µ–∂–¥—É —Ç–æ—á–∫–∞–º–∏ –ø–µ—Ä–µ—Å–µ—á–µ–Ω–∏—è –≥—Ä–∞—Ñ–∏–∫–∞ —Ñ—É–Ω–∫—Ü–∏–∏ —Å –æ—Å—å—é –∞–±—Å—Ü–∏—Å—Å, –∑–Ω–∞—á–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ')


plt.axhline(0, color='red')
plt.axvline(0, color='red')
plt.grid()
plt.plot(x, y)
